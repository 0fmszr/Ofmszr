// Example code using if-else statements

// Define a variable
let num = 10;

// Check if the number is greater than 0
if (num > 0) {
    console.log("The number is positive");
} else if (num === 0) {
    console.log("The number is zero");
} else {
    console.log("The number is negative");
}
//The if-else statement is used to make decisions in your code based on certain conditions.
//It consists of an if block, followed by an optional else block.
//The condition inside the if statement is evaluated. If it is true, the code inside the if block is executed. Otherwise, the code inside the else block is executed.
//In the example code you provided, the variable num is checked to see if it is greater than 0. If 
//it is, the message "The number is positive" is logged to the console. Otherwise, the message "The number is not positive" is logged.
//You can have multiple if-else
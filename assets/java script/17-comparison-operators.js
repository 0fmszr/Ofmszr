// == === operators
const grade = "88";
console.log(grade==88);
console.log(grade===88);


console.log(grade != 78);
console.log(grade !== 99);
console.log(grade!=88);
console.log(grade !== 88);

//== and =! only check inside of variable

// === !== check either variable and data type


